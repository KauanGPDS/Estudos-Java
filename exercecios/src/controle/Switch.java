package controle;

public class Switch {
	public static void main(String[] args) {
		 
		String faixa = "branca";

		   

	    switch (faixa.toLowerCase()) {

	    case "preta":

	    System.out.println("Nível Avançado");

	    case "Vermelha":

	    System.out.println("Nível Avançado");

	    case "Marrom":

	    System.out.println("Nível Avançado");

	    case "Roxa":

	    System.out.println("Nível Intermediário");

	    case "Azul":

	    System.out.println("Nível Intermediário");

	    case "Verde":

	    System.out.println("Nível Intermediário");

	    case "laranja":

	    System.out.println("Nível Iniciante");

	    case "Amarela":

	    System.out.println("Nível Iniciante");

	    case "Branca":

	    System.out.println("Nível Iniciante");

	    default:

	    System.out.println("Faixa inválida");

	      }   

	   }



	}

