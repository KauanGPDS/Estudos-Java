package fundamentos;

public class ConversaoPrimitvo {
	public static void main(String[] args) {
		double a = 1.988888888888888889;
		System.out.println(a);
		
		float b = (float) 1.123459888888888888888888888880;
		System.out.println(b);
		int c = 340;
		byte d = (byte) c ;
		System.out.println(d);
		
		double e = 1.99999;
		int f = (int) e;
		System.out.println(f);
 	}

}
